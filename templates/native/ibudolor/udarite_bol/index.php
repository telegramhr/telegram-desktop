        <h3>95 posto žena pati od menstrualnih bolova</h3>
        <p>Od menstrualnih bolova, primjerice, pati čak 95 posto žena i iako su simptomi najčešće tek osjetni, ponekad mogu biti vrlo jaki te osim bolnog trbuha i leđa mogu izazvati glavobolje, mučnine, proljev i povraćanje. Želite li se boli riješiti brzo i jednostavno, posegnite za  praškom protiv bolova Ibudolor, a važno je ne čekati stvarno jaku bol i grčeve pri kojima se ne možete pomaknuti, već popiti analgetik čim bolovi počnu.</p>
        <h3>Kako protiv menstrualnih bolova </h3>
        <p>Lijekove prve linije čine nesteroidni protuupalni antireumatici. Randomizirane studije pokazuju kako 70 do 90% žena navodi značajno smanjenje bolova na terapiju ibuprofenom u dozi od 400-600 mg svakih 4 do 6 sati. Ibudolor Direkt, jedini ibruprofenski prašak, uzima se bez tekućine i djeluje i do šest sati. Terapiju je najbolje započeti na dan početka menstruacije i nastavlja sve do prestanka krvarenja, odnosno bolova. Kod izraženije kliničke slike početak terapije može se pomaknuti i na dan-dva prije očekivanog početka krvarenja, savjetuju liječnici.</p>
        <h3>Kad naraste temperatura...</h3>
        <p>Čest razlog zašto se poseže za Ibudolorom je i povišena tjelesna temperatura. Tjelesna temperatura se povisi najčešće kao odgovor organizma na infekcije virusima ili bakterijama, a prati je glavobolja, drhtavica, znojenje, gubitak apetita itd. Lijekove koji snižavaju tjelesnu temperaturu treba primijeniti kod vrijednosti tjelesne temperature iznad 38,5°C, a ukoliko povišena temperatura traje više od tri dana i ne može se sniziti lijekovima i fiziološkim mjerama, potrebno je potražiti pomoć liječnika.</p>
