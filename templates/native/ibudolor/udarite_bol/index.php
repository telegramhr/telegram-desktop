        <p>Kod sposobnosti podnošenja boli postoje znatne individualne razlike pa će pojedinci različito reagirati na isti stupanj boli, a dodatno će na to utjecati i situacija i trenutno raspoloženje. Prema trajanju, bol se dijeli na akutnu (kratkotrajnu) i kroničnu (dugotrajnu). </p>
        <h3>Putevi boli</h3>
        <p>Bol počinje na posebnim receptorima za bol koji se nalaze svuda po tijelu. Kada su izloženi nekom bolnom podražaju, oni putem živaca prenose električne impulse u leđnu moždinu i dalje prema mozgu. Tijekom tog puta ti se impulsi obrađuju, stvarajući osjećaj boli, ali i pokreću određene reakcije kojima se nastoji izbjeći ili umanjiti bol, primjerice odmicanje ruke od vrućeg predmeta. Receptori za bol i njihovi živčani putovi razlikuju se u različitim dijelovima tijela pa je zbog toga osjet boli različit u odnosu na vrstu i mjesto pojave.</p>
        <h3>Bol nije bolest, ali može dovesti do bolesti</h3>
        <p>Neliječena akutna bol može prijeći u kroničnu bol što smanjuje kvalitetu života i može dovesti do psiholoških, imunoloških, te patofizioloških promjena u funkcijama tjelesnih sustava. Za bolesnika kronifikacija znači da bol prodire u sve sfere života i loše utječe na mentalnu, kognitivnu i psihosocijalnu funkcionalnost.</p>
