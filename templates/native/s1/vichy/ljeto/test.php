            var c = 0;
            var d = 0;
            var e = 0;
            var result = 1;
            $('.answer').click(function() {
                var active = $('.slide.active').data('slide');
                $current_answer = $(this).attr('data-answer');
                if ($current_answer == 'a') {
                    a++;
                }
                if ($current_answer == 'b') {
                    b++;
                }
                if ($current_answer == 'c') {
                    c++;
                }
                if ($current_answer == 'd') {
                    d++;
                }
                if ($current_answer == 'e') {
                    e++;
                }
                //advance
                $('.slide.slide-' + active).removeClass('active');
                $('.fa.fa-' + active).removeClass('fa-circle');
                active++;
                if (active !== 7) {
                    $('.slide.slide-' + active).addClass('active');
                    $('.fa.fa-' + active).addClass('fa-circle');
                } else {
                    result = 1;
                    biggest_figure = a;
                    if (b > biggest_figure) {
                        result = 2;
                        biggest_figure = b;
                    }
                    if (c > biggest_figure) {
                        result = 3;
                        biggest_figure = c;
                    }
                    if (d > biggest_figure) {
                        result = 4;
                        biggest_figure = d;
                    }
                    if (e > biggest_figure) {
                        result = 5;
                        biggest_figure = e;
                    }
                    a = 0;
                    b = 0;
                    c = 0;
                    d = 0;
                    e = 0;
                    $('.result .question span').text(descriptions[result].title);
                    $('.result .description').html(descriptions[result].text);
                    $('.result .share').attr('href', descriptions[result].url);
                    $('.result .share.tw').data('title', descriptions[result].title);
                    $('.slide.slide-' + active).addClass('active');
                    $('.progress-bar').fadeOut(0);
                }
                return false;
            });

